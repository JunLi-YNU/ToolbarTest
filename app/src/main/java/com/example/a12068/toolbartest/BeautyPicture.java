package com.example.a12068.toolbartest;

public class BeautyPicture {
    private String name;
    private int imageId;

    public BeautyPicture(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }


    public int getImageId() {
        return imageId;
    }
}
